#include <stdio.h>

void main(){

int c, tab, newLine, space, total;

tab = 0;
newLine = 0;
space = 0;
total = 0;

while((c=getchar()) != EOF){

if(c == ' ')
	space ++;
if(c == '\n')
	newLine ++;
if(c == '\t')
	tab ++;


}//while loop counting all characters in text file

total = tab + newLine + space;

printf("Number of spaces: %d\n", space);
printf("Number of tabs: %d\n", tab);
printf("Number of newlines: %d\n", newLine);
printf("Total number of whitespaces: %d\n", total);


}//main method


